// Mengubah tipe data

// to Integer
var a = "123"
intResult = parseInt(a)
console.log(intResult);

// to Float
var b = "123.4"
floatResult = parseFloat(b)
console.log(floatResult);

// to String
var c = 123.4
stringResult = c.toString()
console.log(stringResult)

console.log("")

console.log("intResult = " , typeof(intResult))
console.log("floatResult = " , typeof(floatResult))
console.log("stringResult = " , typeof(stringResult))

